// =======================================================
// 文字コード → font5x7 配列インデックス変換
// =======================================================
uint8_t getFontIndex(const char* c) {
  for(uint8_t i=0;i<sizeof(font5x7)/sizeof(FontChar);i++){
    if(strcmp(font5x7[i].c, c)==0) return i;
  }
  return 0xFF; // 未定義文字は0番目にフォールバック
}

